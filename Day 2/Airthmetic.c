#include <stdio.h>
int main()
{
    int a = 10, b = 30;
    int sum = a + b, sub = a - b, mul = a * b, Division = a / b, Mod = a % b;
    printf("Sum: %d\n", sum);
    printf("Subtraction: %d\n", sub);
    printf("Multiplication: %d\n", mul);
    printf("Division: %d\n",Division);
    printf("Modulus: %d\n",Mod);
    return 0;
}
